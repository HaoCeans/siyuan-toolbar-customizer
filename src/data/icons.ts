/**
 * 更新图标显示
 * @param element - 图标容器元素
 * @param iconValue - 图标值
 * @param iconSize - 图标大小（px），默认16
 */
export function updateIconDisplay(element: HTMLElement, iconValue: string, iconSize: number = 16): void {
  element.innerHTML = ''

  if (!iconValue) {
    element.textContent = '?'
    return
  }

  // 检查是否是思源内置图标（格式：icon开头）
  if (iconValue.startsWith('icon')) {
    element.innerHTML = `<svg style="width: ${iconSize}px; height: ${iconSize}px;"><use xlink:href="#${iconValue}"></use></svg>`
  }
  // 检查是否是 Lucide 图标（格式：lucide:IconName）
  else if (iconValue.startsWith('lucide:')) {
    const iconName = iconValue.substring(7) // 去掉 'lucide:' 前缀
    // 在设置界面显示为文本，因为无法动态加载 lucide
    element.textContent = `[${iconName}]`
    element.style.fontSize = `${Math.max(10, iconSize - 4)}px`
    element.style.fontFamily = 'monospace'
  }
  // 检查是否是图片路径（以 .png/.jpg/.gif/.svg 结尾）
  else if (/\.(png|jpg|jpeg|gif|svg)$/i.test(iconValue)) {
    // 使用绝对路径，思源插件资源路径格式
    const pluginName = 'siyuan-toolbar-customizer'
    const imagePath = iconValue.startsWith('/plugins/') ? iconValue : `/plugins/${pluginName}/${iconValue}`
    element.innerHTML = `<img src="${imagePath}" style="width: ${iconSize}px; height: ${iconSize}px; object-fit: contain;"/>`
  }
  // 否则当作 emoji 或文本
  else {
    element.textContent = iconValue
  }
}
